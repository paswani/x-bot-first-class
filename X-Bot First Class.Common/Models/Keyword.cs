namespace X_Bot_First_Class.Common
{
    /// <summary>
    /// Public class representing a Keyword
    /// </summary>
    public class Keyword
    {
        /// <summary>
        /// Gets or sets the value.
        /// </summary>
        /// <value>
        /// The value.
        /// </value>
        public string Value { get; set; }

        /// <summary>
        /// Gets or sets the source.
        /// </summary>
        /// <value>
        /// The source.
        /// </value>
        public int Source { get; set; }
    }
}
